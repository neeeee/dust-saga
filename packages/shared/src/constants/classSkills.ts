export const CLASS_SPECIFIC_SKILLS: Record<number, Record<string, ClassSpecificSkill>> = {};
for (const [idxStr, names] of Object.entries(JOB_SKILL_NAMES)) {
  const idx = Number(idxStr);
  const overrides = OVERRIDES[idx] || {};
  const job: Record<string, ClassSpecificSkill> = {};
  for (const name of names) {
    const base = SKILLS[name];
    if (!base) continue;
    const override = overrides[name];
    if (override) {
      job[name] = { ...base, ...override } as ClassSpecificSkill;
    } else {
      job[name] = base;
    }
  }
  CLASS_SPECIFIC_SKILLS[idx] = job;
}

